package day05_ArithmeticOperators;

public class DivideByTwoNumbers {
    public static void main(String[] args) {
        int a= 50;
        int b = 9;
        int result = a/b;
        int remainder = a%b;

        System.out.println(a+" Divided by "+b+" is equal to "+result+ " with a remainder of "+remainder);
    }

}
