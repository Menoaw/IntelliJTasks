package day03_Variables;

/*
\n  is for Newline, Advances the cursor to the next line for subsequent printing. (Enter
\t  is for Horizontal Tab, Causes the cursor to skip over to next tab stop. (Tab)
\\ Backslash, causes \ to be printed. (\)
\' Single quote (outdated, no longer used.)
\" Double  quote


 */

public class Escape_Sequences {

    public static void main(String[] args){

        System.out.println("Hello everyone \nHow are you all today?");
        System.out.println("------------------------");

        System.out.println("Hello everyone");
        System.out.println("My name is Matthew");
        System.out.println("I live in Iowa");

        System.out.println("Hello everyone \nMy name is Matthew \nI live in Iowa");

        System.out.println("------------------------");

        System.out.println("\tToday is a great day to study Java");
        System.out.println("------------------------");
        System.out.println("My favorite TV series is \"Supernatural\"");



    }

}
