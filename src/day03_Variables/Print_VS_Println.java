package day03_Variables;

public class Print_VS_Println {
    public static void main(String[] args){
        System.out.println("Hello Batch 23"); //Appends a new line
        System.out.println("How are you today?");

        System.out.println("---------------------");

        System.out.print("Hello Batch 23");//does not add new line (no enter key)
        System.out.print("How are you Today?");// does not add new line

        System.out.println("---------------------");

        System.out.println("JRE stands for Java Runetime Environment, JAVAC stands for Java Compiler, JDK stands for Java Development Kit, JVM stands for Java Virtual Machine.");

        System.out.print("JRE stands for Java Runetime Environment, ");
        System.out.print("JAVAC stands for Java Compiler, ");
        System.out.print("JDK stands for Java Development Kit, ");
        System.out.print("JVM stands for Java Virtual Machine.");



    }


}
