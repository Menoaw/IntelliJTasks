package day03_Variables;

public class CalculateSquare {
    public static void main(String[] args) {

        int w = 8;
        int l = 8;

        int area = w * l;
        int perimeter = 2 * w + 2 * l;
        System.out.print("Width of the Square/Rectangle is: ");
        System.out.println(w);

        System.out.print("Length of the Square/Rectangle is: ");
        System.out.println(l);

        System.out.print("Area of the Square/Rectangle is: ");
        System.out.println(area);

        System.out.print("Perimeter of the Square/Rectangle is: ");
        System.out.println(perimeter);


    }


}
