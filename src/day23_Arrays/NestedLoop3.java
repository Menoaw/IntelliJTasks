package day23_Arrays;

public class NestedLoop3 {
    public static void main(String[] args) {


        for (int i = 1; i <= 8; i++) {
            for (int j = 1; j <= i; j++) {
                System.out.print("*");
            }
            System.out.println();
        }
        System.out.println("---------------------------------------------------");




    }
}


